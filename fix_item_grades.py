def get_grade_info(grade):
    """등급 번호로 이모지와 텍스트 반환"""
    grade_map = {
        1: ('⚪', '일반'),
        2: ('🟢', '고급'), 
        3: ('🔵', '희귀'),
        4: ('🟣', '영웅'),
        5: ('🟠', '전설')
    }
    return grade_map.get(grade, ('⚪', '일반'))

